    private void btn_AgregarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_AgregarMouseClicked
        try{
            this.tipo = cbx_TipoObjeto.getSelectedItem().toString();
            this.nombre = txt_Nombre.getText();
            this.icono = this.getIcono();
            
            if(mario.getCantidad()==1){
                JOptionPane.showMessageDialog(null, "No puedes agregar más de un Mario Bros");
            }
            if(castillo.getCantidad()==1){
                JOptionPane.showMessageDialog(null, "No puedes agregar más de un Castillo");
            }
            
            this.lista.Agregar(new Objeto(nombre, tipo, icono));
            JOptionPane.showMessageDialog(null, "El número de jugadores es " + this.lista.Length());
            
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Error al agregar objeto: "+e);
        }
    }//GEN-LAST:event_btn_AgregarMouseClicked

