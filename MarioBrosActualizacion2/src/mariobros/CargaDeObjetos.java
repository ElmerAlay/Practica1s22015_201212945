    public ImageIcon getIcono(){
            if(cbx_TipoObjeto.getSelectedItem().equals("Mario")){
                this.icono = iconoMario;
                this.mario.setCantidad(1);
            }
            if(cbx_TipoObjeto.getSelectedItem().equals("Castillo")){
                this.icono = iconoCastillo;
                this.castillo.setCantidad(1);
            }
            if(cbx_TipoObjeto.getSelectedItem().equals("Suelo")){
                this.icono = iconoSuelo;
            }
            if(cbx_TipoObjeto.getSelectedItem().equals("Bloque")){
                this.icono = iconoBloque;
            }
            if(cbx_TipoObjeto.getSelectedItem().equals("Goomba")){
                this.icono = iconoGoomba;
            }
            if(cbx_TipoObjeto.getSelectedItem().equals("Koopa")){
                this.icono = iconoKoopa;
            }
            if(cbx_TipoObjeto.getSelectedItem().equals("Ficha")){
                this.icono = iconoFicha;
            }
            if(cbx_TipoObjeto.getSelectedItem().equals("Hondo de Vida")){
      
          this.icono = iconoHongo;
            }
            
            return icono;
    }
